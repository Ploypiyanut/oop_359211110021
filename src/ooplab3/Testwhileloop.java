public class Testwhileloop {
    public static void main(String[] args) {
        // main
               int n = 1;
        while (n <= 10) {
            System.out.println("Hello");
            n++;

        } //while

        // do--while
        do {
            System.out.println("Hello");
            n++;
        } while (n<=10);


    }  // main

}//class
